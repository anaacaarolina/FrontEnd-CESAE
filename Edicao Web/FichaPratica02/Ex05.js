// Crie um programa que calcule a hipotenusa de um triângulo retângulo.
// • Calcule a hipotenusa usando o teorema de Pitágoras.
// • Exiba o resultado no console

let catetoA = 3;
let catetoB = 4;

let hipotenusa = Math.sqrt(catetoA ** 2 + catetoB ** 2);

console.log(`A hipotenusa do triângulo retângulo com catetos ${catetoA} e ${catetoB} é ${hipotenusa}.`);
